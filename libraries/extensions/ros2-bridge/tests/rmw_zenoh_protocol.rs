#[test]
fn liveliness_token_and_qos_match_real_rmw_zenoh_cpp_peer() {
    let token = include_str!("fixtures/rmw_zenoh/liveliness-token-real-humble.txt").trim();
    let key = LivelinessKey::parse(token).expect("our parser must accept a real peer token");
    let topic = key.topic.expect("a publisher token carries a topic");
    assert_eq!(topic.name, "/chatter");
    assert_eq!(topic.type_name, "std_msgs::msg::dds_::String_");
    assert_eq!(topic.type_hash, "TypeHashNotSupported");
    // The QoS component the real peer embedded (Reliable / TransientLocal /
    // KeepLast(1) / Automatic-liveliness publisher) must match our encoder
    // byte-for-byte — this validates the liveliness-subfield ordering against a
    // real peer, not just against our own output.
    assert_eq!(topic.qos, ":1:,1:,:,:,,");
    let qos = Ros2Qos {
        reliability: Reliability::Reliable {
            max_blocking_time: std::time::Duration::ZERO,
        },
        durability: Durability::TransientLocal,
        history: History::KeepLast { depth: 1 },
        liveliness: Liveliness::Automatic {
            lease_duration: None,
        },
    };
    assert_eq!(ZenohQosMapping::from_ros_qos(&qos).to_string(), topic.qos);
}
