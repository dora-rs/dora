fn hex_to_bytes(hex: &str) -> Vec<u8> {
    let hex = hex.trim();
    (0..hex.len())
        .step_by(2)
        .map(|i| u8::from_str_radix(&hex[i..i + 2], 16).expect("valid hex fixture"))
        .collect()
}

#[test]
fn attachment_round_trips_real_rmw_zenoh_cpp_bytes() {
    let real = hex_to_bytes(include_str!(
        "fixtures/rmw_zenoh/attachment-real-humble.hex"
    ));
    let decoded = Attachment::decode(&real).expect("our decoder must accept real peer bytes");
    // Re-encoding the decoded fields must reproduce the exact peer bytes: this
    // proves our attachment codec matches the rmw_zenoh_cpp wire layout — i64
    // sequence, i64 source timestamp, and a length-prefixed 16-byte gid (the
    // `0x10` prefix a prior review doubted is present in both directions).
    assert_eq!(decoded.encode().unwrap(), real);
}

